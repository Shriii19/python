x=100
y=50

# Arithmetic Operators

# addition: +
print("x + y = ", x + y)

# subtraction: -
print("x - y = ", x - y)

# multiplication: *
print("x * y = ", x * y)

# division: /
print("x / y = ", x / y)

# float division: //
print("x // y = ", x // y)

# exponent form: **
print("x ** y = ", x ** y)