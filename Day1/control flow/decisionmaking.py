# WAP FOR SIMPLE if
x = 10
if x > 5:
    print("x is greater than 5")

#WAP FOR if else
x = 10
if x % 2 == 0:
    print("x is even")
else:
    print("x is odd")


#WAP FOR if else ladder
x = 10  
if x > 0:
    print("x is positive")
elif x < 0:
    print("x is negative")
else:
    print("x is zero")

#WAP FOR nested if
x = 10 
if x >= 0:
    if x == 0:
        print("x is zero")
    else:
        print("x is positive")